length=int(input("enter the length of cuboid : "))
hight=int(input("enter the hight of cuboid : "))
breadth=int(input("enter the breadth of cuboid : " ))

area=2*(length*breadth+breadth*hight+hight*length)
perimeter=4*(length*breadth*hight)

print("area of cuboid is :",area)
print("perimeter of cuboid is :",perimeter)
